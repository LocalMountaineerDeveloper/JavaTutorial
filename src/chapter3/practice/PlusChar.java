/*
 * 输出连续的英文字母：使用char型声明'a'~'g'，然后输出它们相加后的结果。
 * */

package chapter3.practice;

public class PlusChar
{
    public static void main(String[] args)
    {
        char a='a';
        char b='b';
        char c='c';
        char d='d';
        char e='e';
        char f='f';
        char g='g';
        System.out.println("’a’~’g’相加后的结果："+(a+b+c+d+e+f+g));
    }
}
